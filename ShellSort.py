def shell_sort(arr):
    """
    Сортировка Шелла.
    """
    n = len(arr)
    # Начинаем с большого промежутка и уменьшаем его
    gap = n // 2

    while gap > 0:
        # Проходим по всем элементам с текущим промежутком
        for i in range(gap, n):
            # Сохраняем текущий элемент
            temp = arr[i]
            j = i

            # Сдвигаем элементы, которые больше temp, вправо на gap позиций
            # до тех пор, пока не найдём правильное место для temp
            while j >= gap and arr[j - gap] > temp:
                arr[j] = arr[j - gap]
                j -= gap

            # Вставляем temp на правильное место
            arr[j] = temp

        # Уменьшаем промежуток
        gap //= 2

# Пример использования
if __name__ == "__main__":
    arr = [12, 34, 54, 2, 3]
    print("Исходный массив:", arr)

    shell_sort(arr)

    print("Отсортированный массив:", arr)
